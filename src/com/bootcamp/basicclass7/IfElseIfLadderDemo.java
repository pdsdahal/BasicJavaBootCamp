package com.bootcamp.basicclass7;

public class IfElseIfLadderDemo {

	public static void main(String[] args) {
	
		if(false) {
			System.out.println("Inside the if block");
		}else if(false) {
			System.out.println("Inside the else if 1 block");
			
		}else if(true) {
			System.out.println("Inside the else if 2 block");
			
		}else if(false) {
			System.out.println("Inside the else if 3 block");
		}
		else {
			System.out.println("Inside the else...");
		}
		
		
		
		
		
	}
}
