package com.bootcamp.basicclass7;

public class IfElseDemo {
	
	public static void main(String[] args) {
		
		int num = 20;
		
		//20 > 10 || 20 < 5 = true || false = true
		if(num > 10 || num < 5) {
			System.out.println("Inside the if...");
		}else {
			System.out.println("Inside the else...");
		}
	
	}

}
