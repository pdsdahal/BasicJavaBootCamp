package com.bootcamp.basicclass9;

public class WhileDemo {
	
	public static void main(String[] args) {
		
		
		int i = 0;
		//0<10 1<10 2<10 3<10, 8<10,9<10, 10<10
		while(i<10) {
			System.out.println("inside the While loop...."+i);
			i++;//i = 0+1=1, 1+1=2, 2+1 = 3......... 8, 8+1=9, 9+1=10
		}
		
		
		
	}

}
